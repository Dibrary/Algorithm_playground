
def countCharacters(words, chars):
    result = 0
    for i in words:
        cnt = 0
        for j in i:
            if j in chars:
                cnt += 1
            else:
                break
        if cnt == len(i):
            result += len(i)
    return result

print(countCharacters(["cat","bt","hat","tree"], "atach"))
print(countCharacters(["hello","world","leetcode"], "welldonehoneyr"))

